      <h2>Visi </h2>
      <p><b>Visi FKIP Universitas Muhammadiyah Kendari adalah Mewujudkan Manusia unggul secara intelektual, emosional dan spiritual dalam bidang Ilmu Pendidikan</b></p>
      <br>
      <h2>Misi </h2>
      <p>
       <ol>
              <li >Menyelenggarakan pendidikan dan pengajaran untuk menyiapkan tenaga pendidik dan tenaga ke pendidikan yang profesional dan berdaya saing global</li>
              <li>
              mengembangkan inovasi metode pendidikan dan pengajaran dalam rangka mewujudkan tujuan pendidikan nasional
              </li>
              <li>
                     Mengembangkan nilai profesionalisme keguruan dalam mewujudkan caturdharma Universittas Muhammadiyah Kendari sebagai agen pembaharuan yang dijiwai oleh semangat ketauhidan
              </li>
              <li>
                     Menyekenggarakan penelitian dalam rangka pendidikan dan pembelajaran serta penemuan dan penambahan khasanah keilmuan dalam rangka pengembangan dan penerapan IPTEK
              </li>
              <li>
                     Mengembangkan budaya atmosfir akademik dalam kehidupan kampus yang edukatif, ilmiah dan religius.
              </li>
       </ol>
      </p>

      <h2>Tujuan </h2>
      <p>Tujuan FKIP adalah menjadi fakultas unggul pada tahun 2020 yang mampu menghasilkan lulusan yang berkualitas sebagai berikut :</p>
      <p>
       <ol>
              <li >Berakhalak mulia dan memiliki integritas kebpribadian yang tinggi </li>
              <li>
                     Memiliki  kemampuan dalam bidang pengadministrasian sekolah bagi lulusan program studi administrasi pendidikan
              </li>
              <li>Memiliki kemampuan untuk menjadi tenaga pendidik bagi lulusan bahasa inggris, pendidikan guru anak usia dini dan pendidilan luar sekolah 
              </li>
              <li>
                     Memiliki jiwa mandiri dan kreatif sehingga mampu berfikir, bersikap dan bertindak sebagai pendidik maupun wirausahawan
              </li>
       </ol>
      </p>
